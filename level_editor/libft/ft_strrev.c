#include "libft.h"

void	ft_strrev(char *s)
{
	size_t		i;
	size_t		j;
	char		tmp;

	i = 0;
	j = ft_strlen(s);
	while (i < j--)
	{
		tmp = s[i];
		s[i++] = s[j];
		s[j] = tmp;
	}
}
