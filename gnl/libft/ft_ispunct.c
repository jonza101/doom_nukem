#include "libft.h"

int		ft_ispunct(int c)
{
	if (!((c >= 0 && c <= 32) || (c >= 48 && c <= 57) ||
	(c >= 65 && c <= 91) || (c >= 97 && c <= 122)))
		return (1);
	return (0);
}
